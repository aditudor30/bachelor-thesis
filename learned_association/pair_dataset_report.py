"""Markdown reports for the Person association pair dataset."""

from pathlib import Path
from typing import Any, Dict, Sequence


def write_pair_dataset_report(
    output_root: Path,
    source_summary: Dict[str, Any],
    verdict: Dict[str, Any],
    fragments: Sequence[Dict[str, Any]],
    pairs: Sequence[Dict[str, Any]],
) -> None:
    """Write the requested human-readable report and README."""
    labels = verdict.get("label_distribution", {})
    valid_fragments = sum(bool(row.get("valid_for_pairs")) for row in fragments)
    embedding_fragments = sum(bool(row.get("embedding_available")) for row in fragments)
    text = """# Person Association Pair Dataset Report

## Purpose

This Step 20A artifact builds supervised Person fragment pairs for a future learned
multi-camera association scorer. It does not train a model or alter the existing V1/V2
tracking pipeline.

## Sources

- Fragment source: `{source_name}`
- Fragment root: `{source_root}`
- Identity supervision: train/val ground truth only
- Identity namespace: `scene_name + object_id`

## Dataset Size

- Fragments loaded: {fragments}
- Fragments valid for pairs: {valid_fragments}
- Fragments with embeddings: {embedding_fragments}
- Candidate pairs: {pairs}
- Positive pairs: {positive_pairs}
- Negative pairs: {negative_pairs}
- Hard negatives: {hard_negatives}

## Quality Notes

- ReID, temporal, camera, pseudo3D, motion, quality, and conflict features are exported.
- Missing ReID or geometry is represented explicitly and does not stop dataset creation.
- Train and validation remain scene-disjoint.
- Unknown or low-purity GT fragment matches are excluded from supervised pairs.

## Verdict

`{verdict}`

Reasons/warnings: {reasons}

## Step 20B Recommendation

`ready_for_step_20b = {ready}`. If true, train only on the balanced train CSV and tune
thresholds/model selection on the balanced validation CSV. Keep raw candidate pairs for
ablation and missing-feature analysis.
""".format(
        source_name=source_summary.get("source_name", "unknown"),
        source_root=source_summary.get("source_root", "unknown"),
        fragments=len(fragments),
        valid_fragments=valid_fragments,
        embedding_fragments=embedding_fragments,
        pairs=len(pairs),
        positive_pairs=labels.get("positive_pairs", 0),
        negative_pairs=labels.get("negative_pairs", 0),
        hard_negatives=labels.get("hard_negatives", 0),
        verdict=verdict.get("verdict"),
        reasons=", ".join(verdict.get("reasons", [])) or "none",
        ready=str(bool(verdict.get("ready_for_step_20b"))).lower(),
    )
    reports_dir = output_root / "reports"
    reports_dir.mkdir(parents=True, exist_ok=True)
    (reports_dir / "PERSON_ASSOCIATION_PAIR_DATASET_REPORT.md").write_text(text, encoding="utf-8")
    readme = """# Learned Person Association Pair Dataset

Generated by `build_person_association_pair_dataset`. The `metadata/` directory contains
fragment and candidate-pair provenance, `pairs/` contains model-ready raw and balanced
splits, and `diagnostics/` contains readiness checks. No test GT is used.
"""
    (reports_dir / "README_PERSON_ASSOCIATION_PAIR_DATASET.md").write_text(readme, encoding="utf-8")
